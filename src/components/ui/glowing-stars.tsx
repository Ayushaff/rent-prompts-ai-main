"use client";

import React, { useEffect, useRef, useState } from "react";
import { AnimatePresence, motion } from "framer-motion";
import { cn } from "@/utilities/cn";

export const GlowingStarsBackgroundCard = ({
  className,
  children,
}: {
  className?: string;
  children?: React.ReactNode;
}) => {
  const [mouseEnter, setMouseEnter] = useState(false);

  return (
    <div

      className={cn(
        `bg-gradient-to-br from-indigo-600 to-indigo-700 p-4 max-w-full h-full w-full rounded-lg border-2 border-muted-foreground hover:border-gray-300 transition-all hover:scale-[1.02] hover:shadow-lg hover:shadow-blue-gray-500/20 focus:scale-[1.02] focus:opacity-[0.85] focus:shadow-none active:scale-100 active:opacity-[0.85] active:shadow-none disabled:pointer-events-none disabled:opacity-50 disabled:shadow-none`,
        className
      )}
    >
      <div className="">{children}</div>
    </div>
  );
};


export const GlowingStarsBackground = ({
    className,
    children,
  }: {
    className?: string;
    children?: React.ReactNode;
  }) => {
    const [mouseEnter, setMouseEnter] = useState(false);

    return (
      <div
        onMouseEnter={() => {
            setMouseEnter(true);
        }}
        onMouseLeave={() => {
            setMouseEnter(false);
        }}
        className={cn(
          "px-10",
          className
        )}
      >
        <Illustration mouseEnter={mouseEnter} />
      </div>
    );
  };


